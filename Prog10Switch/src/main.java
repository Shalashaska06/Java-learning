
public class main {

	public static void main(String[] args) {
		
		
		String jour = "Vendredi";
		
		switch(jour) {
		
		case "Dimanche": System.out.println("on est dimanche !");
		break;
		case "Lundi": System.out.println("on est lundi!");
		break;
		case "Mardi": System.out.println("on est mardi");
		break;
		case "Mercredi": System.out.println("on est Mercredi !");
		break;
		case "Jeudi": System.out.println("on est jeudi");
		break;
		case "Vendredi": System.out.println("on est Vendredi !");
		break;
		case "Samedi": System.out.println("on est samedi !");
		break;
		default : System.out.println("C'est la semaine des quatres jeudis !");
		
		}

	}

}
